#!/usr/bin/env python3

"""
A function that takes two strings and concatenates them.
"""


def concat(str1: str, str2: str) -> str:
    """
    A function that takes two string parameters and returns
    a concatenated string.

    @Params:
        str1 (str): First string argument.
        str2 (str): Second string argument.

    Returns:
        str: Returning concatenated string

    """
    return str1 + str2
